import UIKit

extension UIColor {
    
    @nonobjc class var AppThemeColor: UIColor {
        return UIColor(red: 33.0 / 255.0, green: 172.0 / 255.0, blue: 2.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var bgcolor: UIColor {
        return UIColor(red: 54.0 / 255.0, green: 150.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var AppNotifColor: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 0.5)
    }
    
    @nonobjc class var highlite: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 154.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
    
}
